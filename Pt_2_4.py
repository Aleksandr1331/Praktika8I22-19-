print("Программа для решения квадратных уравний формы ax2 + bx + c = 0")
a = float(input("Введите значение коэффицентов:\n\ta = "))
b = float(input("\tb = "))
c = float(input("\tc = "))
D = b ** 2 - 4 * a * c

if D > 0:
    print("Уравнение имеет 2 корня:")
    print(f"\tX1 = {((-b + (D**0.5)) / (2 * a))}")
    print(f"\tX2 = {((-b - (D**0.5)) / (2 * a))}")

elif D == 0:
    print("Уравнение имеет 1 корень:\n\tX =", (-b / (2 * a)))
else:
    print("Корней нет")
